default_font = '8x6'

def str_dim(text, font=default_font, font_scale=1, final_sep=True):
    f = fonts[font]
    fh = f['height']
    FONT = f['data']
    y = font_scale * fh
    x_list = []
    x = 0
    for c in text:
        if c == '\n':
            y += font_scale * fh
            x = 0
        else:
            if c == '\r':
                pass
            else:
                c = ord(c)
                if c < f['bounds'][0] or c > f['bounds'][1]:
                    c_data = f['undef']
                else:
                    c_data = FONT[(c - f['bounds'][0])]
                fw = len(c_data)
                fw += f['sep']
                x += font_scale * fw
        x_list.append(x)
        if not final_sep:
            x_list = [xi - font_scale * f['sep'] for xi in x_list]

    return (
     max(x_list), y)


